CREATE TABLE Employee (
  ID bigint AUTO_INCREMENT PRIMARY KEY,
  FIRSTNAME VARCHAR(250) NOT NULL,
  LASTNAME VARCHAR(250) NOT NULL,
  CONTACTNUMBER VARCHAR(250) NOT NULL
